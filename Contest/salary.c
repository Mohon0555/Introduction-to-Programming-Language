/* A program to find income tax of an employee according to salary */
#include<stdio.h>

int main() {
  int i;
  for (i = 1; i < 6; i++) {
    printf("\n%d ", i);
    i = i + 1;
  }
  for (i = 4; i >= 1; i = i - 1) {
    printf("%d ", i);
  }
  return 0;
}

